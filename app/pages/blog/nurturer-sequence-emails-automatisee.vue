      <ol class="mt-2 list-decimal pl-5 text-default space-y-2">
        <li>
          <strong>Email 1 – Bienvenue & cadeau</strong>
          <ul class="mt-2 list-disc pl-5 space-y-1">
            <li>Remerciez la personne pour sa confiance</li>
            <li>Offrez le contenu promis (guide, méditation, etc.)</li>
            <li>Présentez-vous brièvement (qui vous êtes, votre mission)</li>
          </ul>
          <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">🎁 Exemple : « Voici votre guide offert + un mot de bienvenue »</blockquote>
        </li>
        <li>
          <strong>Email 2 – Votre histoire</strong>
          <ul class="mt-2 list-disc pl-5 space-y-1">
            <li>Racontez pourquoi vous faites ce métier</li>
            <li>Montrez que vous comprenez les difficultés de votre audience</li>
            <li>Créez une connexion émotionnelle</li>
          </ul>
          <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">« Moi aussi, j’ai connu cette période de doute… »</blockquote>
        </li>
        <li>
          <strong>Email 3 – Partage de valeur</strong>
          <ul class="mt-2 list-disc pl-5 space-y-1">
            <li>Donnez un conseil pratique ou un exercice simple</li>
            <li>Montrez votre expertise sans jargon</li>
            <li>Restez dans une logique d’aide</li>
          </ul>
          <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">« Voici 3 étapes pour calmer le mental en période de stress. »</blockquote>
        </li>
        <li>
          <strong>Email 4 – Témoignage ou étude de cas</strong>
          <ul class="mt-2 list-disc pl-5 space-y-1">
            <li>Partagez l’histoire d’une personne que vous avez accompagnée</li>
            <li>Montrez la transformation obtenue</li>
            <li>Faites sentir ce qui est possible</li>
          </ul>
          <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">« Comment Sophie a retrouvé confiance en elle après 3 séances. »</blockquote>
        </li>
        <li>
          <strong>Email 5 – Invitation douce</strong>
          <ul class="mt-2 list-disc pl-5 space-y-1">
            <li>Proposez un appel découverte, un atelier ou un programme</li>
            <li>Expliquez ce qu’il va y gagner</li>
            <li>Rassurez : pas de pression, juste un échange</li>
          </ul>
          <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">« Si vous sentez que c’est le bon moment, voici le lien pour en parler. »</blockquote>
        </li>
      </ol>
      <p class="mt-3 text-default">💡 Cette structure fonctionne parce qu’elle <strong>guide pas à pas</strong> le lecteur de la découverte à la décision.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">✨ Étape 3 : Écrivez avec le ton juste</h2>
      <p class="mt-3 text-default">Votre séquence ne doit pas « vendre », mais <strong>accompagner</strong>. Écrivez comme si vous parliez à une seule personne — une personne que vous avez envie d’aider.</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Utilisez le <strong>« vous »</strong> personnel</li>
        <li>Parlez <strong>simplement et avec empathie</strong></li>
        <li>Partagez vos expériences avec authenticité</li>
        <li>Évitez les phrases trop marketing</li>
      </ul>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">⚙️ Étape 4 : Automatisez pour rester disponible… même hors ligne</h2>
      <p class="mt-3 text-default">Programmez l’envoi automatique après chaque inscription avec vos outils (ex. <strong>GoHighLevel</strong>, <strong>Systeme.io</strong>, <strong>Mailerlite</strong>...).</p>
      <p class="mt-3 text-default">Rythme conseillé : <strong>tous les 2 à 3 jours</strong> — assez régulier pour garder le lien, sans envahir.</p>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">💡 L’automatisation n’est pas froide : elle vous permet d’être <strong>présent en continu</strong>, même lorsque vous n’êtes pas derrière votre écran.</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🪶 Étape 5 : Soignez la fin de votre séquence</h2>
      <p class="mt-3 text-default">Le dernier email doit clore la séquence positivement, rappeler votre mission et ouvrir une porte vers la suite (newsletter, appel, accompagnement).</p>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">« Merci d’avoir suivi ces emails. J’espère qu’ils vous auront inspiré. Si vous souhaitez qu’on en parle ensemble, je vous offre un appel découverte. »</blockquote>

