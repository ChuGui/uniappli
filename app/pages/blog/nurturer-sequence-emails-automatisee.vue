      </h1>

      <p class="mt-6 text-base sm:text-lg text-default">Un visiteur s’est inscrit sur votre page, a téléchargé votre ressource, ou s’est abonné à votre newsletter. <br/>Félicitations 🎉 Mais maintenant, une question se pose : <strong>comment transformer ce contact en client sans être “trop commercial” ?</strong></p>
      <p class="mt-4 text-base sm:text-lg text-default">La réponse : <strong>en construisant une relation de confiance</strong> grâce à une <strong>séquence d’emails bienveillante et automatisée</strong>.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌱 Pourquoi une séquence d’emails change tout</h2>
      <p class="mt-3 text-default">Beaucoup de coachs laissent leurs contacts “dormir” après le premier téléchargement. Résultat : le lien se perd, et la personne vous oublie.</p>
      <p class="mt-3 text-default">Une <strong>séquence d’emails</strong> est une suite de messages envoyés automatiquement après l’inscription. Elle permet de :</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Garder le lien avec vos nouveaux abonnés</li>
        <li>Les aider à mieux vous connaître</li>
        <li>Les préparer naturellement à votre accompagnement</li>
      </ul>
      <blockquote class="mt-4 border-l-4 border-default pl-4 italic text-default">C’est un peu comme un premier rendez-vous où vous prenez le temps d’écouter, partager et créer la confiance.</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🧭 Étape 1 : Définissez votre intention</h2>
      <p class="mt-3 text-default">Avant d’écrire le moindre mot, demandez-vous : <q>Quel est l’objectif de cette séquence ?</q></p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Apprendre à connaître vos valeurs et votre approche</li>
        <li>Aider la personne à mieux comprendre son problème</li>
        <li>L’amener naturellement vers un appel découverte ou une offre</li>
      </ul>
      <p class="mt-3 text-default">💬 Votre intention doit être <strong>d’aider avant de vendre</strong>. La vente viendra ensuite, comme une continuité logique.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🧩 Étape 2 : Structurez une séquence simple et humaine</h2>
