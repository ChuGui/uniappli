        <span class="text-primary-gradient font-lobster drop-shadow-md text-4xl sm:text-5xl">Capturer l’intérêt grâce à une landing page</span>
      </h1>

      <p class="mt-6 text-base sm:text-lg text-default">Vous avez commencé à partager du contenu, à être visible, et certaines personnes s’intéressent à ce que vous faites. Mais beaucoup partent sans jamais revenir. Pourquoi ? Parce qu’il n’y a <strong>pas de point d’entrée clair</strong> vers votre univers.</p>
      <p class="mt-4 text-base sm:text-lg text-default">C’est ici qu’intervient la <strong>landing page</strong> — ou <strong>page d’inscription</strong>. C’est un espace simple et dédié qui transforme un visiteur curieux… en contact réel.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">💡 Pourquoi une landing page est essentielle</h2>
      <p class="mt-3 text-default">Votre site, vos réseaux, vos publications inspirent la confiance. Mais pour qu’un inconnu devienne un futur client, il faut une <strong>étape intermédiaire</strong>.</p>
      <p class="mt-3 text-default">La landing page sert à :</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Attirer l’attention d’un visiteur intéressé</li>
        <li>Lui proposer une action claire et utile</li>
        <li>Récupérer son <strong>prénom et son email</strong> pour garder le lien</li>
      </ul>
      <blockquote class="mt-4 border-l-4 border-default pl-4 italic text-default">C’est la différence entre “avoir de la visibilité” et “créer une audience engagée”.</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🎯 Étape 1 : Choisissez un objectif simple et unique</h2>
      <p class="mt-3 text-default">Une erreur fréquente est de vouloir tout mettre sur la même page. Résultat : le visiteur se perd.</p>
      <p class="mt-3 text-default">Une bonne landing page a <strong>un seul objectif</strong>.</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>S’inscrire à votre newsletter</li>
        <li>Télécharger un guide gratuit</li>
        <li>Réserver un appel découverte</li>
      </ul>
      <p class="mt-3 text-default">💬 Choisissez <strong>un seul appel à l’action</strong> clair et mettez-le en avant.</p>
      <blockquote class="mt-3 border-l-4 border-default pl-4 italic text-default">Exemple : “Téléchargez votre guide offert : 5 étapes pour retrouver confiance et sérénité.”</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌟 Étape 2 : Offrez une vraie valeur dès le départ</h2>
      <p class="mt-3 text-default">Votre visiteur ne vous connaît pas encore. Il a besoin d’une <strong>raison</strong> pour laisser son email. Offrez-lui <strong>quelque chose d’utile et concret</strong> :</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Un <strong>ebook</strong> (“5 exercices pour calmer l’anxiété”)</li>
        <li>Une <strong>checklist</strong> (“Les 7 clés pour oser se lancer”)</li>
        <li>Une <strong>mini-formation vidéo</strong></li>
        <li>Une <strong>méditation guidée</strong></li>
      </ul>
      <p class="mt-3 text-default">👉 L’important, c’est que ce contenu aide immédiatement la personne à avancer. Elle se dira : “Si ce cadeau gratuit m’a déjà aidé, j’imagine ce que son accompagnement pourrait m’apporter.”</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🧭 Étape 3 : Structurez votre page simplement</h2>
      <p class="mt-3 text-default">Votre landing page n’a pas besoin d’être longue. Elle doit être <strong>claire, visuelle et apaisante</strong>.</p>
      <p class="mt-3 text-default">Structure simple :</p>
      <ol class="mt-2 list-decimal pl-5 text-default space-y-1">
        <li><strong>Un titre percutant</strong> qui parle du bénéfice concret</li>
        <li><strong>Une phrase d’accroche</strong> qui explique la promesse</li>
        <li><strong>Un visuel</strong> (photo ou illustration)</li>
        <li><strong>Un formulaire court</strong> – prénom + email suffisent</li>
        <li><strong>Un bouton d’action</strong> clair</li>
        <li><strong>Une petite section de réassurance</strong> (pas de spam, désinscription possible)</li>
      </ol>
      <p class="mt-3 text-default">🎨 Le design doit être simple et cohérent avec votre univers (couleurs, ton, typographie).</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">✨ Étape 4 : Connectez votre page à un système d’emails</h2>
      <p class="mt-3 text-default">Une landing page seule ne suffit pas : il faut ensuite <strong>entretenir la relation</strong>.</p>
      <ol class="mt-2 list-decimal pl-5 text-default space-y-1">
        <li>Un <strong>email de bienvenue</strong> (avec le lien vers le cadeau)</li>
        <li>Une <strong>séquence d’emails bienveillante</strong> qui crée le lien</li>
      </ol>
      <p class="mt-3 text-default">💡 Vous pouvez automatiser cela avec <strong>GoHighLevel</strong>, <strong>Systeme.io</strong> ou <strong>MailerLite</strong>.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🪶 Étape 5 : Parlez avec votre cœur</h2>
      <p class="mt-3 text-default">Une landing page, ce n’est pas de la vente : c’est une <strong>invitation</strong>.</p>
      <blockquote class="mt-3 border-l-4 border-default pl-4 italic text-default">“Si ce que je partage vous parle, voici une première ressource pour vous aider.”</blockquote>
      <p class="mt-3 text-default">Soyez simple, sincère, et mettez-vous à la place de votre lecteur.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🧩 Étape 6 : Inspirez confiance</h2>
      <ul class="mt-3 list-disc pl-5 text-default space-y-1">
        <li>Ajoutez votre <strong>photo</strong></li>
        <li>Glissez un <strong>témoignage</strong> client si vous en avez</li>
        <li>Mentionnez vos <strong>valeurs</strong> en une phrase</li>
        <li>Évitez les promesses “miracles”</li>
      </ul>
      <blockquote class="mt-3 border-l-4 border-default pl-4 italic text-default">Une landing page est une <strong>rencontre digitale</strong>. Plus elle reflète qui vous êtes, plus elle attire les bonnes personnes.</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌼 En résumé</h2>
      <ol class="mt-3 list-decimal pl-5 text-default space-y-1">
        <li><strong>Un objectif unique et clair</strong></li>
        <li><strong>Un cadeau utile et désirable</strong></li>
        <li><strong>Une structure simple et fluide</strong></li>
        <li><strong>Une connexion automatique avec vos emails</strong></li>
        <li><strong>Une touche d’humanité et de sincérité</strong></li>
      </ol>
      <blockquote class="mt-4 border-l-4 border-default pl-4 italic text-default">Vous n’avez pas besoin d’un grand site pour commencer. Une seule page claire et connectée peut devenir le <strong>point d’entrée</strong> de tout votre système.</blockquote>

