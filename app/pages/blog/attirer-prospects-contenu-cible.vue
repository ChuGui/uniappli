        <li><strong>Fréquence :</strong> 1 à 2 publications par semaine suffisent.</li>
        <li><strong>Planification :</strong> préparez vos idées à l’avance dans un tableau ou un carnet.</li>
      </ol>
      <p class="mt-3 text-default">💡 Astuce : si vous avez déjà des échanges intéressants avec vos clients, transformez-les en contenu. Ce que vous dites en séance est souvent ce que d’autres ont besoin d’entendre.</p>

