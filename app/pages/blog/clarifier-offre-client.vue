<template>
  <article class="px-4 sm:px-6 lg:px-8 py-12 sm:py-16">
    <div class="mx-auto w-full max-w-3xl">
      <h1 class="text-3xl sm:text-4xl font-bold leading-tight text-default">
        <span class="text-primary-gradient font-lobster drop-shadow-md text-4xl sm:text-5xl">Voici comment clarifier votre offre et votre client cible</span>
      </h1>

      <p class="mt-6 text-base sm:text-lg text-default">Quand on démarre comme coach ou thérapeute, on veut souvent <strong>aider tout le monde</strong>.<br />Mais à vouloir parler à tout le monde, on finit par ne toucher personne.</p>
      <p class="mt-4 text-base sm:text-lg text-default">Cette première étape – <strong>clarifier votre offre et votre client cible</strong> – est la base de tout système d’acquisition. C’est ce qui permet à vos messages de parler directement à ceux que vous voulez vraiment aider.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌱 Pourquoi la clarté change tout</h2>
      <p class="mt-4 text-default">Imaginez deux phrases :</p>
      <ul class="mt-3 space-y-2 text-default">
        <li>• “J’aide les gens à se sentir mieux dans leur vie.”</li>
        <li>• “J’aide les femmes en reconversion à dépasser leurs peurs pour créer leur activité.”</li>
      </ul>
      <p class="mt-3 text-default">La seconde attire l’attention car elle est <strong>spécifique</strong>. Votre futur client se reconnaît immédiatement.</p>
      <blockquote class="mt-4 border-l-4 border-default pl-4 italic text-default">Plus votre message est clair, plus il devient magnétique.</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">✨ 1. Identifiez votre client idéal</h2>
      <p class="mt-3 text-default">Avant toute communication, définissez <strong>à qui vous voulez parler</strong>.</p>
      <p class="mt-3 text-default">Posez-vous ces questions :</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Qui avez-vous le plus envie d’aider ?</li>
        <li>Quel est leur problème principal ?</li>
        <li>Quel résultat désirent-ils vraiment ?</li>
      </ul>
      <p class="mt-3 text-default">Exemples :</p>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">“J’aide les entrepreneurs à se libérer du stress.”</blockquote>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">“J’aide les femmes à retrouver confiance pour oser changer de vie.”</blockquote>
      <p class="mt-3 text-default">💡 Parlez d’émotions et de situations concrètes, pas seulement de critères comme l’âge ou la profession. Vos clients se reconnaissent dans une <strong>expérience vécue</strong>.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">❤️ 2. Clarifiez le problème que vous résolvez</h2>
      <p class="mt-3 text-default">Un coach ou un thérapeute n’est pas payé “pour aider”, mais pour <strong>résoudre un problème précis</strong>. Formulez votre promesse simplement :</p>
      <blockquote class="mt-3 border-l-4 border-default pl-4 italic text-default">“J’aide [type de personne] à [résoudre un problème] pour [obtenir un résultat].”</blockquote>
      <p class="mt-3 text-default">Exemples :</p>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>“J’aide les parents épuisés à retrouver sérénité et plaisir dans leur quotidien.”</li>
        <li>“J’aide les femmes timides à reprendre confiance pour s’exprimer librement.”</li>
      </ul>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌟 3. Créez une offre claire et rassurante</h2>
      <p class="mt-3 text-default">Évitez les “séances à l’unité”. Proposez plutôt un <strong>accompagnement structuré</strong> avec un objectif précis.</p>
      <blockquote class="mt-3 border-l-4 border-default pl-4 italic text-default">Exemple : “Programme Confiance – 3 mois pour retrouver assurance et sérénité.”</blockquote>
      <h3 class="mt-4 font-semibold text-default">Pour structurer votre offre :</h3>
      <ul class="mt-2 list-disc pl-5 text-default space-y-1">
        <li>Définissez une <strong>durée claire</strong> (ex. 3 mois).</li>
        <li>Précisez le <strong>format</strong> (en ligne, présentiel, individuel…).</li>
        <li>Mettez en avant un <strong>résultat concret</strong>.</li>
        <li>Donnez-lui un <strong>nom simple et inspirant</strong> (“Clarté”, “Reconnexion”, “Renouveau”…).</li>
      </ul>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🧭 4. Trouvez les bons mots</h2>
      <p class="mt-3 text-default">Le marketing, ce n’est pas de la manipulation. C’est simplement <strong>expliquer clairement</strong> comment vous aidez.</p>
      <p class="mt-3 text-default">Utilisez la structure suivante :</p>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">“J’aide [type de personne] à [objectif], parce que je crois que [valeur].<br/>J’utilise [outil/méthode] dans un accompagnement [format].”</blockquote>
      <p class="mt-3 text-default">Exemple :</p>
      <blockquote class="mt-2 border-l-4 border-default pl-4 italic text-default">“J’aide les femmes à se reconnecter à leur confiance, parce que je crois que chacun mérite de vivre aligné avec soi. J’utilise le coaching et la PNL dans un parcours de 3 mois.”</blockquote>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🔍 5. Testez et ajustez</h2>
      <p class="mt-3 text-default">Ne cherchez pas la perfection dès le départ. Testez votre message auprès de votre entourage ou sur les réseaux. Voyez ce qui touche, ce qui résonne, et ajustez.</p>
      <p class="mt-3 text-default">Chaque conversation est une <strong>boussole</strong> qui vous rapproche de la clarté.</p>

      <hr class="my-8 border-default" />

      <h2 class="text-2xl sm:text-3xl font-semibold text-default">🌼 En résumé</h2>
      <p class="mt-3 text-default">Clarifier votre offre et votre client cible, c’est :</p>
      <ol class="mt-2 list-decimal pl-5 text-default space-y-1">
        <li>Savoir <strong>à qui</strong> vous parlez</li>
        <li>Comprendre <strong>ce qu’ils vivent</strong></li>
        <li>Expliquer <strong>comment vous les aidez</strong></li>
        <li>Proposer <strong>un accompagnement structuré</strong></li>
        <li>Trouver <strong>les mots justes et humains</strong></li>
      </ol>
      <blockquote class="mt-4 border-l-4 border-default pl-4 italic text-default">Vous n’avez pas besoin de “faire du marketing”. Vous avez besoin de <strong>savoir exprimer votre mission avec clarté et sincérité</strong>.</blockquote>

      <div class="mt-10 flex flex-col sm:flex-row gap-3 sm:gap-4">
